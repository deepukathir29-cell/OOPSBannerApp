/**
 * OOPSBannerApp UC2 - ASCII Banner for OOPS
 * Prints OOPS using 7-line, 9-width grid format using asterisks (*) and spaces( ) to form each letter
 */
public class OOPSBannerApp {
   public static void main(String[] args) {
        System.out.println("  *****  " + "  *****  " + "********" + "  ****** ");
        System.out.println(" **   ** " + " **   ** " + "**     *" + " **     *");
        System.out.println("**     **" + "**     **" + "**      *" + "**       ");
        System.out.println("**     **" + "**     **" + "******** " + "  ****** ");
        System.out.println("**     **" + "**     **" + "**       " + "       **");
        System.out.println(" **   ** " + " **   ** " + " **      " + "*      **");
        System.out.println("  *****  " + "  *****  " + " **      " + " ******  ");
   }
}